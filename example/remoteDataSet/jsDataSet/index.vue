<script>
import { cloneDeep } from 'lodash'
import { nameCheckRepeat, datasetAdd, datasetUpdate, getDataset, getCategoryTree } from 'packages/js/utils/datasetConfigService'
import { codemirror } from 'vue-codemirror'
import 'codemirror/mode/javascript/javascript'
import 'codemirror/lib/codemirror.css'
import 'codemirror/theme/nord.css'
export default {
  name: 'JsDataSet',
  components: {
    codemirror
  },
  props: {
    config: {
      type: Object,
      default: () => {}
    },
    isEdit: {
      type: Boolean,
      default: false
    },
    datasetId: {
      type: String,
      default: null
    },
    typeId: {
      type: String,
      default: ''
    },
    appCode: {
      type: String,
      default: ''
    }
  },
  data () {
    const validateName = (rule, value, callback) => {
      nameCheckRepeat({
        id: this.datasetId,
        name: value,
        moduleCode: this.appCode
      }).then((r) => {
        if (r) {
          callback(new Error('数据集名称已存在'))
        } else {
          callback()
        }
      })
    }
    return {
      dataForm: {
        id: '',
        name: '',
        typeId: '',
        remark: '',
        config: {
          script: '',
          paramsList: []
        }
      },
      rules: {
        name: [
          { required: true, message: '请输入数据集名称', trigger: 'blur' },
          { validator: validateName, trigger: 'blur' }
        ]
      },
      codemirrorOption: {
        mode: 'text/javascript',
        lineNumbers: true,
        lineWrapping: true,
        theme: 'nord',
        extraKey: { Ctrl: 'autocomplete' },
        hintOptions: {
          completeSingle: true
        }
      },
      activeName: 'data',
      dataPreviewList: [],
      structurePreviewList: [],
      structurePreviewListCopy: [],
      typeName: '',
      categoryData: [],
      fieldDescVisible: false,
      fieldsetVisible: false,
      paramsVisible: false,
      tableLoading: false,
      saveloading: false,
      saveText: '',
      paramsListCopy: [],
      isSet: false, // 参数是否配置状态
      passTest: false,
      fieldDesc: null // 字段描述
    }
  },
  watch: {
    'dataForm.config.script' (val) {
      if (!val) {
        this.passTest = false
      }
    }
  },
  mounted () {
    this.init()
  },
  methods: {
    async init () {
      this.categoryData = await getCategoryTree({ type: 'dataset', moduleCode: this.appCode })
      if (this.typeId) {
        this.dataForm.typeId = this.typeId
        this.$nextTick(() => {
          try {
            this.typeName = this.$refs.categorySelectTree.getNode(this.dataForm.typeId).data.name
          } catch (error) {
            console.error(error)
          }
        })
      }
      if (this.datasetId) {
        getDataset(this.datasetId).then(res => {
          this.dataForm.id = res.id
          this.dataForm.name = res.name
          this.dataForm.typeId = res.typeId
          this.dataForm.remark = res.remark
          this.paramsListCopy = cloneDeep(this.dataForm.config.paramsList)
          this.dataForm.config.script = res.config.script
          this.dataForm.config.paramsList = res.config.paramsList
          this.fieldDesc = res.config.fieldDesc
          this.structurePreviewList = res.config.fieldList
          this.scriptExecute(true)
        })
      }
    },
    // 保存数据集
    save (formName, nochecktosave = false) {
      if (this.passTest === false) {
        this.$message.error('请确保脚本不为空且执行通过')
        return
      }
      if (!this.structurePreviewList.length) {
        this.$message.warning('该执行脚本未生成输出字段，请重新检查')
        return
      }
      if (!nochecktosave) {
        const temp = this.structurePreviewList.some(item => {
          return item.fieldDesc === '' || !item.hasOwnProperty('fieldDesc')
        }) // true-存在为空
        if (temp) {
          this.fieldDescVisible = true
          return
        }
      }
      this.$refs[formName].validate((valid) => {
        if (valid) {
          this.saveloading = true
          this.saveText = '正在保存...'
          const form = {
            id: this.datasetId,
            name: this.dataForm.name,
            typeId: this.dataForm.typeId,
            remark: this.dataForm.remark,
            datasetType: this.config.datasetType,
            moduleCode: this.appCode,
            editable: this.appCode ? 1 : 0,
            config: {
              className: this.config.className,
              script: this.dataForm.config.script,
              fieldDesc: this.fieldDesc,
              paramsList: [],
              fieldList: this.structurePreviewList
            }
          }
          const datasetSave = this.dataForm.id === '' ? datasetAdd : datasetUpdate
          datasetSave(form).then(() => {
            this.$message.success('操作成功')
            this.$parent.init(false)
            this.$parent.setType = null
            this.saveloading = false
            this.saveText = ''
          }).catch(() => {
            this.saveloading = false
            this.saveText = ''
          })
        }
      })
    },
    // 取消操作
    cancelField () {
      this.structurePreviewListCopy = cloneDeep(this.structurePreviewList)
      this.fieldsetVisible = false
    },
    // 设置输出字段
    setField () {
      this.structurePreviewList = cloneDeep(this.structurePreviewListCopy)
      if (this.structurePreviewList.length) {
        this.fieldDesc = {}
        this.structurePreviewList.forEach(key => {
          this.fieldDesc[key.fieldName] = key.fieldDesc
        })
      } else {
        this.fieldDesc = null
      }
      this.fieldsetVisible = false
    },
    // 字段值填充
    fieldDescFill () {
      this.fieldDesc = {}
      this.structurePreviewList.forEach(field => {
        if (field.fieldDesc === '' || !field.hasOwnProperty('fieldDesc')) {
          field.fieldDesc = field.fieldName
          this.fieldDesc[field.fieldName] = field.fieldName
        } else {
          this.fieldDesc[field.fieldName] = field.fieldDesc
        }
      })
      this.save('form')
      this.fieldDescVisible = false
    },
    // 进入编辑
    fieldDescEdit () {
      this.fieldDescVisible = false
      this.fieldsetVisible = true
    },
    // 继续保存
    toSave () {
      this.fieldDesc = {}
      this.structurePreviewList.forEach(field => {
        this.fieldDesc[field.fieldName] = field.fieldDesc
      })
      this.save('form', true)
      this.fieldDescVisible = false
    },
    // 字段描述构建及同步
    buildFieldDesc () {
      const fieldDesc = {}
      this.structurePreviewList.forEach(field => {
        if (this.fieldDesc.hasOwnProperty(field.fieldName)) {
          field.fieldDesc = this.fieldDesc[field.fieldName]
        }
        fieldDesc[field.fieldName] = field.fieldDesc
      })
      this.fieldDesc = fieldDesc
    },
    // 脚本执行
    scriptExecute (isInit = false) {
      if (this.dataForm.config.script) {
        const javascript = this.dataForm.config.script
        let scriptMethod = null
        scriptMethod = eval(`(${javascript})`)
        // 调用方法生成随机数据
        const scriptData = scriptMethod()
        //  检查数据是否为数组
        if (!Array.isArray(scriptData)) {
          this.passTest = false
          this.$message.error('脚本执行结果不是数组，请检查脚本')
          return
        }
        const keys = []
        scriptData.forEach(item => {
          Object.keys(item).forEach(key => {
            if (!keys.includes(key)) {
              keys.push(key)
            }
          })
        })
        this.structurePreviewList = keys.map(item => {
          return {
            fieldName: item,
            fieldDesc: ''
          }
        })
        if (this.structurePreviewList.length && this.fieldDesc) {
          this.buildFieldDesc()
        }
        // 如果有数据，就通过测试
        if (this.structurePreviewList.length > 0) {
          this.structurePreviewListCopy = cloneDeep(this.structurePreviewList)
          this.dataPreviewList = this.structurePreviewList
          this.passTest = true
          if (!isInit) {
            this.$message.success('脚本执行通过')
          }
        } else {
          this.passTest = false
        }
      } else {
        this.passTest = false
        this.$message.error('请填写脚本')
      }
    },
    // 执行事件
    toExecute () {
      if (this.dataForm.config.paramsList.length) {
        this.isSet = false
        this.paramsVisible = true
      } else {
        // 无参数，直接执行脚本
        this.scriptExecute()
      }
    },
    // 清空分类
    clearType () {
      this.typeName = ''
      this.dataForm.typeId = ''
    },
    // 分类展开高亮
    setCurrentNode ($event) {
      if ($event) {
        const key = this.dataForm.typeId || null
        this.$refs.categorySelectTree.setCurrentKey(key)
      }
    },
    // 分类选择
    selectParentCategory (value) {
      this.dataForm.typeId = value.id
      this.typeName = value.name
      this.$refs.selectParentName.blur()
    },
    goBack () {
      this.$emit('back')
    },
    renderHeader (h, { column, index }) {
      const labelLong = column.label.length // 表头label长度
      const size = 14 // 根据需要定义标尺，直接使用字体大小确定就行，也可以根据需要定义
      column.minWidth = labelLong * size < 120 ? 120 : labelLong * size // 根据label长度计算该表头最终宽度
      return h('span', { class: 'cell-content', style: { width: '100%' } }, [column.label])
    },
    openNewWindow (url) {
      window.open(url, '_blank')
    }
  }
}
</script>

